from mcp_anything.models.analysis import AnalysisResult, Capability, IPCType, ParameterSpec
from mcp_anything.pipeline.scope import apply_scope, read_scope_file, write_scope_file


def _make_analysis(capabilities: list[Capability]) -> AnalysisResult:
    return AnalysisResult(
        app_name="test-app",
        app_description="Test application",
        capabilities=capabilities,
        primary_ipc=IPCType.PROTOCOL,
    )


def _cap(name: str, source: str = "", description: str = "", category: str = "api") -> Capability:
    return Capability(
        name=name,
        description=description or f"Description for {name}",
        category=category,
        source_file=source,
        ipc_type=IPCType.PROTOCOL,
    )


@pytest.fixture
def sample_analysis():
    return _make_analysis([
        _cap("get_users", "/api/v2/users", "GET /api/v2/users - List users"),
        _cap("get_user", "/api/v2/users/{id}", "GET /api/v2/users/{id} - Get user"),
        _cap("create_user", "/api/v2/users", "POST /api/v2/users - Create user"),
        _cap("get_internal_metrics", "/internal/metrics", "GET /internal/metrics"),
        _cap("get_internal_health", "/internal/health", "GET /internal/health"),
        _cap("get_reports", "/api/v1/reports", "GET /api/v1/reports - Legacy reports"),
        _cap("debug_dump", "debug/dump.py", "Debug dump endpoint", category="debug"),
    ])


# --- write_scope_file / read_scope_file ---

class TestScopeFileIO:
    def test_write_read_roundtrip(self, sample_analysis, tmp_path):
        scope_path = tmp_path / "scope.yaml"
        write_scope_file(sample_analysis, scope_path)

        assert scope_path.exists()
        doc = read_scope_file(scope_path)

        assert "capabilities" in doc
        assert len(doc["capabilities"]) == 7
        assert all("enabled" not in entry for entry in doc["capabilities"])
        assert doc["include_patterns"] == []
        assert doc["exclude_patterns"] == []

    def test_write_includes_metadata(self, tmp_path):
        analysis = _make_analysis([
            Capability(
                name="get_items",
                description="GET /api/items",
                category="api",
                source_file="routes/items.py",
                ipc_type=IPCType.PROTOCOL,
            ),
        ])
        scope_path = tmp_path / "scope.yaml"
        write_scope_file(analysis, scope_path)

        doc = read_scope_file(scope_path)
        entry = doc["capabilities"][0]
        assert entry["name"] == "get_items"
        assert entry["description"] == "GET /api/items"
        assert entry["source"] == "routes/items.py"
        assert entry["category"] == "api"

    def test_write_creates_parent_dirs(self, tmp_path):
        scope_path = tmp_path / "deep" / "nested" / "scope.yaml"
        analysis = _make_analysis([_cap("test_cap")])
        write_scope_file(analysis, scope_path)
        assert scope_path.exists()

    def test_read_invalid_file(self, tmp_path):
        bad_file = tmp_path / "bad.yaml"
        bad_file.write_text("just a string")
        with pytest.raises(ValueError, match="expected a YAML mapping"):
            read_scope_file(bad_file)

    def test_scope_file_has_header_comments(self, tmp_path):
        analysis = _make_analysis([_cap("test_cap")])
        scope_path = tmp_path / "scope.yaml"
        write_scope_file(analysis, scope_path)

        content = scope_path.read_text()
        assert "Generated by mcp-anything" in content
        assert "--resume" in content


# --- apply_scope with include patterns ---

class TestIncludePatterns:
    def test_include_by_name(self, sample_analysis):
        result = apply_scope(sample_analysis, include_patterns=["get_user*"])
        names = [c.name for c in result.capabilities]
        assert "get_users" in names
        assert "get_user" in names
        assert "create_user" not in names
        assert "get_internal_metrics" not in names

    def test_include_by_source_path(self, sample_analysis):
        result = apply_scope(sample_analysis, include_patterns=["/api/v2/*"])
        names = [c.name for c in result.capabilities]
        assert "get_users" in names
        assert "get_user" in names
        assert "create_user" in names
        assert "get_internal_metrics" not in names
        assert "get_reports" not in names

    def test_include_by_description(self, sample_analysis):
        result = apply_scope(sample_analysis, include_patterns=["*List users*"])
        names = [c.name for c in result.capabilities]
        assert names == ["get_users"]

    def test_include_multiple_patterns(self, sample_analysis):
        result = apply_scope(
            sample_analysis,
            include_patterns=["get_users", "get_reports"],
        )
        names = [c.name for c in result.capabilities]
        assert set(names) == {"get_users", "get_reports"}

    def test_include_no_match(self, sample_analysis):
        result = apply_scope(sample_analysis, include_patterns=["nonexistent*"])
        assert len(result.capabilities) == 0


