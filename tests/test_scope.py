# --- apply_scope with scope file ---

class TestScopeFile:
    def test_scope_file_disable_capabilities(self, sample_analysis, tmp_path):
        scope_path = tmp_path / "scope.yaml"
        write_scope_file(sample_analysis, scope_path)

        # Edit: disable internal capabilities
        doc = read_scope_file(scope_path)
        for entry in doc["capabilities"]:
            if "internal" in entry["name"]:
                entry["enabled"] = False
        with open(scope_path, "w") as f:
            yaml.dump(doc, f)

        result = apply_scope(sample_analysis, scope_file=scope_path)
        names = [c.name for c in result.capabilities]
        assert "get_internal_metrics" not in names
        assert "get_internal_health" not in names
        assert "get_users" in names

    def test_scope_file_with_patterns(self, tmp_path):
        analysis = _make_analysis([
            _cap("get_users", "/api/users"),
            _cap("get_admin", "/admin/users"),
            _cap("get_debug", "/debug/info"),
        ])

        scope_path = tmp_path / "scope.yaml"
        doc = {
            "include_patterns": [],
            "exclude_patterns": ["/admin/*", "/debug/*"],
            "capabilities": [],
        }
        with open(scope_path, "w") as f:
            yaml.dump(doc, f)

        result = apply_scope(analysis, scope_file=scope_path)
        names = [c.name for c in result.capabilities]
        assert names == ["get_users"]

    def test_scope_file_explicit_enable_overrides_exclude_pattern(self, tmp_path):
        """Explicitly enabled capabilities bypass pattern filters."""
        analysis = _make_analysis([
            _cap("get_internal_metrics", "/internal/metrics"),
            _cap("get_internal_health", "/internal/health"),
        ])

        scope_path = tmp_path / "scope.yaml"
        doc = {
            "include_patterns": [],
            "exclude_patterns": ["*internal*"],
            "capabilities": [
                {"name": "get_internal_metrics", "enabled": True},
            ],
        }
        with open(scope_path, "w") as f:
            yaml.dump(doc, f)

        result = apply_scope(analysis, scope_file=scope_path)
        names = [c.name for c in result.capabilities]
        # Explicitly enabled: kept despite exclude pattern
        assert "get_internal_metrics" in names
        # Not in capabilities list: excluded by pattern
        assert "get_internal_health" not in names

    def test_scope_file_combined_with_cli_patterns(self, sample_analysis, tmp_path):
        """CLI patterns are additive with scope file patterns."""
        scope_path = tmp_path / "scope.yaml"
        doc = {
            "include_patterns": [],
            "exclude_patterns": ["*internal*"],
            "capabilities": [],
        }
        with open(scope_path, "w") as f:
            yaml.dump(doc, f)

        result = apply_scope(
            sample_analysis,
            exclude_patterns=["debug_*"],
            scope_file=scope_path,
        )
        names = [c.name for c in result.capabilities]
        assert "get_internal_metrics" not in names
        assert "debug_dump" not in names
        assert "get_users" in names

    def test_scope_file_nonexistent_is_skipped(self, sample_analysis, tmp_path):
        result = apply_scope(
            sample_analysis,
            scope_file=tmp_path / "nonexistent.yaml",
        )
        assert len(result.capabilities) == len(sample_analysis.capabilities)


# --- edge cases ---

class TestEdgeCases:
    def test_empty_analysis(self):
        analysis = _make_analysis([])
        result = apply_scope(analysis, include_patterns=["*"])
        assert len(result.capabilities) == 0

    def test_all_excluded(self, sample_analysis):
        result = apply_scope(sample_analysis, exclude_patterns=["*"])
        assert len(result.capabilities) == 0

    def test_no_filters_returns_all(self, sample_analysis):
        result = apply_scope(sample_analysis)
        assert len(result.capabilities) == len(sample_analysis.capabilities)

    def test_original_analysis_not_mutated(self, sample_analysis):
        original_count = len(sample_analysis.capabilities)
        apply_scope(sample_analysis, exclude_patterns=["*"])
        assert len(sample_analysis.capabilities) == original_count

    def test_capabilities_without_source_file(self):
        analysis = _make_analysis([
            _cap("bare_cap", source="", description="A bare capability"),
        ])
        result = apply_scope(analysis, include_patterns=["bare_*"])
        assert len(result.capabilities) == 1

    def test_scope_file_missing_capabilities_key(self, tmp_path):
        scope_path = tmp_path / "scope.yaml"
        doc = {"exclude_patterns": ["*internal*"]}
        with open(scope_path, "w") as f:
            yaml.dump(doc, f)

        analysis = _make_analysis([
            _cap("get_users", "/api/users"),
            _cap("get_internal", "/internal/x"),
        ])
        result = apply_scope(analysis, scope_file=scope_path)
        names = [c.name for c in result.capabilities]
        assert "get_users" in names
        assert "get_internal" not in names


# --- integration: review + resume workflow ---

class TestReviewResumeWorkflow:
    def test_full_review_resume_cycle(self, sample_analysis, tmp_path):
        """Simulate: analyze --review → user edits → --resume."""
        scope_path = tmp_path / "scope.yaml"

        # Step 1: write scope file (what --review does)
        write_scope_file(sample_analysis, scope_path)
        assert scope_path.exists()

        # Step 2: user edits — disables internal + debug, keeps the rest
        doc = read_scope_file(scope_path)
        for entry in doc["capabilities"]:
            if "internal" in entry["name"] or "debug" in entry["name"]:
                entry["enabled"] = False
        with open(scope_path, "w") as f:
            yaml.dump(doc, f)

        # Step 3: resume applies the scope file
        result = apply_scope(sample_analysis, scope_file=scope_path)
        names = [c.name for c in result.capabilities]

        assert "get_users" in names
        assert "get_user" in names
        assert "create_user" in names
        assert "get_reports" in names
        assert "get_internal_metrics" not in names
        assert "get_internal_health" not in names
        assert "debug_dump" not in names
        assert len(result.capabilities) == 4

    def test_review_then_add_exclude_patterns(self, sample_analysis, tmp_path):
        """User edits scope.yaml to add exclude patterns instead of per-cap."""
        scope_path = tmp_path / "scope.yaml"
        write_scope_file(sample_analysis, scope_path)

        doc = read_scope_file(scope_path)
        doc["exclude_patterns"] = ["/internal/*", "debug_*"]
        # Don't touch per-capability flags — all stay enabled
        with open(scope_path, "w") as f:
            yaml.dump(doc, f)

        result = apply_scope(sample_analysis, scope_file=scope_path)
        names = [c.name for c in result.capabilities]
        assert "get_internal_metrics" not in names
        assert "get_internal_health" not in names
        assert "debug_dump" not in names
        assert "get_users" in names
